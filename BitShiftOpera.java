public class BitShiftOpera {
    public static void main(String[] args) {

        byte x=8,y=-8;

        System.out.println("bitwise leftShift = "+ (x<<2));
        System.out.println("bitwise RightShift = "+ (x>>2));
        System.out.println("bitwise leftShift = "+ (y<<2));
        System.out.println("bitwise RightShift = "+ (y>>2));
        System.out.println(" Unsigned bitwise leftShift = "+ (x>>>2));
        System.out.println(" Unsigned bitwise leftShift = "+ (x>>>2));
    }
}
