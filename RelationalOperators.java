import java.util.*;
public class RelationalOperators {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("enter any two numbers to get it's output regarding to relational operators= ");
        int a=sc.nextInt();
        int b=sc.nextInt();

        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}

